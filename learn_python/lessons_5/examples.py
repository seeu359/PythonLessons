text = 'code'
for symbol in text:
    print(symbol)


def reverse_string(text):
    # Начальное значение
    result = ''
    # char - переменная, в которую записывается текущий символ
    for char in text:
        # Соединяем в обратном порядке
        result = char + result
    # Цикл заканчивается, когда пройдена вся строка
    return result


_result = reverse_string('go!')  # => '!og'

print(_result)